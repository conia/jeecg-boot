package org.jeecg.modules.demo.om.entity;


public class OmModelRunData {
    private String fileId;
    private String fileName;
    private String filePath;
    private String datasetId;
    private String datasetFmtId;
    private String datasetFmt;

    public String getFileId() {
        return fileId;
    }

    public void setFileId(String fileId) {
        this.fileId = fileId;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public String getFilePath() {
        return filePath;
    }

    public void setFilePath(String filePath) {
        this.filePath = filePath;
    }

    public String getDatasetId() {
        return datasetId;
    }

    public void setDatasetId(String datasetId) {
        this.datasetId = datasetId;
    }

    public String getDatasetFmtId() {
        return datasetFmtId;
    }

    public void setDatasetFmtId(String datasetFmtId) {
        this.datasetFmtId = datasetFmtId;
    }

    public String getDatasetFmt() {
        return datasetFmt;
    }



    public void setDatasetFmt(String datasetFmt) {
        this.datasetFmt = datasetFmt;
    }
}
